def display_help
  puts "yaru -- a simple todo list CLI ✔︎"
  puts "\n"
  puts "Add a todo item:        yaru a <item>"
  puts "List all todo items:    yaru l"
  puts "Check off a todo item:  yaru c <item number>"
  puts "Uncheck a todo item:    yaru uc <item number>"
  puts "Edit a todo item:       yaru e <item number>"
  puts "Delete a todo item:     yaru d <item number>"
end